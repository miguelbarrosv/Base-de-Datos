SELECT * 
FROM JUGADORES J, EQUIPOS E
WHERE UPPER(J.PROCEDENCIA) IN ('SLOVENIA','SPAIN') 
AND UPPER(E.NOMBRE) = 'LAKERS';

--2
SELECT E.NOMBRE, J.PESO*0.4535, J.NOMBRE
FROM EQUIPOS E, JUGADORES J
WHERE J.PESO BETWEEN 150 AND 300;

--3
SELECT J.NOMBRE, E.NOMBRE
FROM JUGADORES J, EQUPOS E
WHERE J.PROCEDENCIA IS NULL;

--4
SELECT E.NOMBRE, E.CONFERENCIA
FROM EQUIPOS E, JUGADORES J
WHERE UPPER(J.NOMBRE) LIKE 'R_ _ _ _ _S';

